# Faça um Programa que peça as 4 notas bimestrais e mostre a média. 

print("Vamos Calcular a Média de 4 Notas?")

print("Digite a 1ª Nota")
nota01 = input()

print("Digite a 2ª Nota")
nota02 = input()

print("Digite a 3ª Nota")
nota03 = input()

print("Digite a 4ª Nota")
nota04 = input()

media = (int(nota01) + int(nota02) + int(nota03) + int(nota04)) / 4

print("A Média das 4 notas é: ", media)